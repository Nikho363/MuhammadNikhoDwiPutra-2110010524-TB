    public void Tampilkandata() {
        daftarTBL_Penumpang = new DefaultTableModel();
        daftarTBL_Penumpang.addColumn("ID Penerbangan");
        daftarTBL_Penumpang.addColumn("Maskapai");
        daftarTBL_Penumpang.addColumn("Rute");        
        daftarTBL_Penumpang.addColumn("Keberangkatan");
        daftarTBL_Penumpang.addColumn("Kedatangan");
        daftarTBL_Penumpang.addColumn("Kelas");
        daftarTBL_Penumpang.addColumn("Harga Tiket");
        
        tbPenerbangan.setModel(daftarTBL_Penumpang);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from penerbangan";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()){
                daftarTBL_Penumpang.addRow(new Object[]{
                    res.getString("id_penerbangan"),
                    res.getString("maskapai"),        
                    res.getString("rute"),
                    res.getString("jam_keberangkatan"), 
                    res.getString("jam_kedatangan"), 
                    res.getString("kelas"), 
                    res.getString("harga_tiket") }); 
                    
            }
            
        int i = daftarTBL_Penumpang.getRowCount();
        lbJumlahData.setText(""+i);
            
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
