    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {          
            if (tfIdPenerbangan.getText().isEmpty() || tfMaskapai.getText().isEmpty() || tfRute.getText().isEmpty()
                || tfJamKeberangkatan.getText().isEmpty() || tfJamKedatangan.getText().isEmpty() || tfKelas.getText().isEmpty()
                || tfHargaTiket.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Semua kolom harus diisi");
                    return; 
                }
            
            if (isIdExists(tfIdPenerbangan.getText())) {
                JOptionPane.showMessageDialog(this, "ID sudah terdaftar. Gunakan ID lain.");
                return;
            }
            
            String sql = "INSERT INTO penerbangan VALUES"
                    + "('"+tfIdPenerbangan.getText()+"','"+tfMaskapai.getText()+"','"+tfRute.getText()+"',"
                    + "'"+tfJamKeberangkatan.getText()+"','"+tfJamKedatangan.getText()+"','"+tfKelas.getText()+"',"
                    + "'"+tfHargaTiket.getText()+"')";
            Connection conn = Koneksi.getConnection();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        Tampilkandata();
        kosong();
    }//GEN-LAST:event_jButton1ActionPerformed

